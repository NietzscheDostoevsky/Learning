class Airliner {

    public static void main(String[] args) {
        short numberOfEngines = 4;
        short seatingCapacity = 300;
        short maximumAltitudeInFeet = 30000;
        short maximumSpeedInKmph = 900;
        short numberOfCrewMembers = 15;

        System.out.println("Number of engines: " + numberOfEngines);
        System.out.println("Seating capacity: " + seatingCapacity);
        System.out.println("Maximum altitude (feet): " + maximumAltitudeInFeet);
        System.out.println("Maximum speed (kmph): " + maximumSpeedInKmph);
        System.out.println("Number of crew members: " + numberOfCrewMembers);
    }
}
