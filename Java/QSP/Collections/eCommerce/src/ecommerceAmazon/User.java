package ecommerceAmazon;

public class User {
	String userName;
	String password;
	String phoneNumber;
	String address;
	
	public User(String userName, String password, String phoneNumber, String address) {
		super();
		this.userName = userName;
		this.password = password;
		this.phoneNumber = phoneNumber;
		this.address = address;
	} 
	
	
}
