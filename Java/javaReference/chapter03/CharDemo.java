class CharDemo {
    public static void main(String[] args) {
        char ch1, ch2;
        ch1 = 88; // code for X (UNIODE)
        ch2 = 'Y';

        System.out.print("ch1 and ch2 : ");
        System.out.println(ch1 + " " + ch2);

    }
}

// java uses UNICODE by default,
// but the first 127 values on Unicode are same as
// ASCII character set.