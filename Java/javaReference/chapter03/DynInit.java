
// Demonstrating dynamic initilization 
class DynInit {
    public static void main(String[] args) {
        double a = 3.0 , b = 4.0 ; 
        
        // variable c is dynamically initializad 

        double c = Math.sqrt( a * a + b * b ) ; 

        System.out.println("Hypotenous is " + c ) ; 

    }
}