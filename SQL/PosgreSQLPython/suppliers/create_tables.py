# To create a new table in a PostgreSQL database, 
# you use the following steps:
# ---------------------------------------------------------
# First, connect to the PostgreSQL server by 
# calling the connect() function. 
# The connect() function returns a connection object.
# Second, create a cursor object by 
# calling the cursor() method of the connection object.
# Third, execute the CREATE TABLE by 
# calling the execute() method of the cursor object.
# Finally, close the connection.
# ---------------------------------------------------------